async def get_hint(scenario_id: int, step: int, question: str) -> tuple[str, str]:
    """Возвращает (текст_подсказки, источник): 'static' или 'llm:GigaChat-2'."""
    static = STATIC_HINTS.get(scenario_id, {}).get(step)
    if static:
        return static, "static"

    context_message = (
        f"Пользователь проходит сценарий #{scenario_id}, шаг {step + 1}. "
        f"Его вопрос: «{question}»"
    )
    reply, model = await ask_llm(
        [{"role": "user", "content": context_message}],
        system=HINT_PROMPT,
    )
    return reply, f"llm:{model}"
